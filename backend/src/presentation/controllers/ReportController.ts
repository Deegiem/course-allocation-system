interface PDFOptions {
  title: string;
  subtitle?: string;
  header?: any;
  infoCard: any;
  statsCards: any;
  table: any;
  footerRef?: string;
  extraContent?: any[];
}

function createStandardPDF(options: PDFOptions): any {
  const content = [];
  
  // Header
  content.push(options.header || createEnhancedHeader());
  
  // Title
  content.push({
    text: options.title,
    fontSize: 24,
    bold: true,
    color: COLORS.grayDark,
    alignment: 'center',
    margin: [0, 16, 0, 4],
    font: DEFAULT_FONT,
  });
  
  if (options.subtitle) {
    content.push({
      text: options.subtitle,
      fontSize: 16,
      color: COLORS.primary,
      alignment: 'center',
      margin: [0, 0, 0, 20],
      font: DEFAULT_FONT,
    });
  }
  
  // Info Card
  content.push(options.infoCard);
  
  // Stats Cards
  content.push(options.statsCards);
  
  // Table
  content.push(options.table);
  
  // Extra Content
  if (options.extraContent) {
    content.push(...options.extraContent);
  }
  
  return {
    pageSize: 'A4',
    pageMargins: [35, 55, 35, 55],
    defaultStyle: {
      font: DEFAULT_FONT,
    },
    content,
    footer: (currentPage: number, pageCount: number) => 
      createEnhancedFooter(currentPage, pageCount, options.footerRef),
  };
}

