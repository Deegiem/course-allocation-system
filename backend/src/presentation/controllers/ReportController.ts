// Enhanced Info Card with better styling
function createEnhancedInfoCard(data: {
  generatedAt: string;
  reportType: string;
  generatedBy: string;
  totalRecords: number;
  ref?: string;
  extra?: { label: string; value: string; color?: string }[];
}): any {
  const rows = [
    ['Generated On', `${formatDate(data.generatedAt)} • ${formatTime(data.generatedAt)}`],
    ['Report Type', data.reportType],
    ['Generated By', data.generatedBy],
    ['Total Records', `${data.totalRecords} ${data.reportType.includes('Course') ? 'Courses' : 'Records'}`],
  ];

  if (data.ref) {
    rows.push(['Reference', data.ref]);
  }

  if (data.extra) {
    for (const item of data.extra) {
      rows.push([item.label, { text: item.value, color: item.color || COLORS.grayDark }]);
    }
  }

  return {
    table: {
      widths: ['30%', '70%'],
      body: [
        [
          {
            text: 'REPORT INFORMATION',
            colSpan: 2,
            bold: true,
            fontSize: 13,
            color: COLORS.primary,
            margin: [0, 0, 0, 8],
            font: DEFAULT_FONT,
          },
          {},
        ],
        ...rows.map(row => [
          { 
            text: row[0], 
            bold: true, 
            color: COLORS.secondary, 
            fontSize: 10, 
            font: DEFAULT_FONT,
            margin: [0, 4, 0, 4],
          },
          typeof row[1] === 'string' 
            ? { text: row[1], color: COLORS.grayDark, fontSize: 10, font: DEFAULT_FONT, margin: [0, 4, 0, 4] }
            : { ...row[1], margin: [0, 4, 0, 4] }
        ]),
      ],
    },
    layout: {
      hLineWidth: () => 1,
      hLineColor: () => COLORS.border,
      paddingLeft: () => 8,
      paddingRight: () => 8,
      paddingTop: () => 4,
      paddingBottom: () => 4,
    },
    margin: [0, 0, 0, 20],
  };
}

