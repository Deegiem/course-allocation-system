// Enhanced Table with better styling
function createEnhancedTable(
  columns: string[], 
  rows: any[][], 
  columnWidths: string[],
  options?: { 
    headerColor?: string; 
    alternateRowColor?: string;
    rowColors?: (rowIndex: number, data: any) => string;
  }
): any {
  const headerColor = options?.headerColor || COLORS.primary;
  
  const headerRow = columns.map(text => ({
    text: text.toUpperCase(),
    bold: true,
    color: COLORS.white,
    fillColor: headerColor,
    padding: [10, 8, 10, 8],
    fontSize: 9,
    font: DEFAULT_FONT,
    alignment: 'center',
  }));

  return {
    table: {
      headerRows: 1,
      widths: columnWidths,
      body: [
        headerRow,
        ...rows.map((row, rowIndex) => {
          const fillColor = rowIndex % 2 === 0 ? COLORS.grayLighter : COLORS.white;
          return row.map((cell, cellIndex) => ({
            text: cell,
            fontSize: 9,
            padding: [8, 6, 8, 6],
            color: COLORS.grayDark,
            fillColor: options?.rowColors ? options.rowColors(rowIndex, cell) : fillColor,
            font: DEFAULT_FONT,
            alignment: cellIndex === 0 ? 'center' : 'left',
          }));
        })
      ],
    },
    layout: {
      fillColor: (rowIndex: number) => {
        if (rowIndex === 0) return headerColor;
        return rowIndex % 2 === 0 ? COLORS.grayLighter : COLORS.white;
      },
      hLineWidth: (rowIndex: number) => {
        return rowIndex === 0 ? 0 : 0.5;
      },
      hLineColor: () => COLORS.border,
      vLineWidth: () => 0,
    },
    margin: [0, 0, 0, 16],
  };
}

